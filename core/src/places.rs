//! Capital cities of the world's countries, used as selectable observer
//! locations on the globe. Coordinates are approximate (degrees, north/east
//! positive); names are kept ASCII so they render in the default UI font.

use crate::math::DVec3;

#[derive(Debug, Clone, Copy)]
pub struct Capital {
    pub name: &'static str,
    pub country: &'static str,
    pub lat: f64,
    pub lon: f64,
}

impl Capital {
    /// Unit position in the body-fixed (ITRS) frame: a point on the sphere at
    /// this geodetic latitude/longitude (spherical approximation, fine for
    /// placing a marker on the rendered globe).
    pub fn itrs_unit(&self) -> DVec3 {
        itrs_unit(self.lat, self.lon)
    }
}

/// Unit vector on the sphere for a geodetic latitude/longitude (degrees).
pub fn itrs_unit(lat_deg: f64, lon_deg: f64) -> DVec3 {
    let (sla, cla) = lat_deg.to_radians().sin_cos();
    let (slo, clo) = lon_deg.to_radians().sin_cos();
    DVec3::new(cla * clo, cla * slo, sla)
}

pub fn capitals() -> &'static [Capital] {
    CAPITALS
}

